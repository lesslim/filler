#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char		*dp;
	const unsigned char	*sp;

	dp = dst;
	sp = src;
	while (n != 0)
	{
		if ((*dp++ = *sp++) == (unsigned char)c)
			return (dp);
		n--;
	}
	return (0);
}
