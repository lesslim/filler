#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*ret;

	ret = dst;
	while (1)
	{
		if (!len--)
			return (ret);
		else if ((*dst++ = *src++) == 0)
			break ;
	}
	while (len--)
		*dst++ = 0;
	return (ret);
}
