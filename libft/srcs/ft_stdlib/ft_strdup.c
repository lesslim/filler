#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*ret;

	len = ft_strlen(s1) + 1;
	if (!(ret = malloc(sizeof(*s1) * len)))
		return (0);
	ft_memcpy(ret, s1, len);
	return (ret);
}
